<h1>Who we Bee</h1>
<p>So our great-grandfather was a beekeeper. He did it for most
    of his life. He didn't have an elaborate set up, he didn't even own a bee suit.
    He kept his bees outside his kitchen door right next to his house, and if you
    wanted honey or a chunk of comb he would just go out pop the lid and take
    what he needed.

    Fast-forward a few years and my brother, and I decided to get into beekeeping
    We started with 2 hives and within the first month we had lost one. But we kept
    going and 5 years later we have a few more. &#x1f41d;

    So I decided to make this website in hopes of sharing my mistakes and what
    I have learned the past few years in hopes of helping and inspiring others.


